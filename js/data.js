function initData() {
  const storedCats = JSON.parse(localStorage.getItem('qds_categories') || '[]');
  const seedCatNames = seedData.categories.map(c => c.name).sort().join(',');
  const storedCatNames = storedCats.map(c => c.name).sort().join(',');
  if (storedCatNames !== seedCatNames) {
    localStorage.setItem('qds_categories', JSON.stringify(seedData.categories));
  }

  if (!localStorage.getItem('qds_variants')) {
    localStorage.setItem('qds_variants', JSON.stringify(seedData.variants));
  }
  if (!localStorage.getItem('qds_analytics')) {
    localStorage.setItem('qds_analytics', JSON.stringify(seedData.analytics));
  }

  const stored = JSON.parse(localStorage.getItem('qds_products') || '[]');
  if (stored.length === 0) {
    localStorage.setItem('qds_products', JSON.stringify(seedData.products));
  } else {
    const merged = seedData.products.map(seed => {
      const existing = stored.find(p => p.id === seed.id);
      return existing ? { ...seed, ...existing, image: seed.image } : seed;
    });
    const localOnly = stored.filter(p => !seedData.products.find(s => s.id === p.id));
    localStorage.setItem('qds_products', JSON.stringify([...merged, ...localOnly]));
  }

  const cats = JSON.parse(localStorage.getItem('qds_categories') || '[]');
  const prods = JSON.parse(localStorage.getItem('qds_products') || '[]');
  const updatedCats = cats.map(c => {
    const seed = seedData.categories.find(item => item.name === c.name);
    return {
      ...c,
      image: seed ? seed.image : c.image,
      productCount: prods.filter(p => p.category === c.name).length
    };
  });
  localStorage.setItem('qds_categories', JSON.stringify(updatedCats));
}

